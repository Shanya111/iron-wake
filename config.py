"""Централизованные настройки торгового движка.

Здесь собраны все «магические числа» стратегий: таймфреймы, глубина истории,
окна поиска уровней, пороги объёма и пробоя, интервалы планировщика. Раньше они
были разбросаны по коду — теперь в одном месте, чтобы легко калибровать.

Часть порогов (объём, глубина пробоя) пользователь может менять на лету через
команду /settings — они хранятся в settings.json рядом с кодом и переопределяют
значения по умолчанию. Остальные константы меняются правкой этого файла.
"""

import json
from pathlib import Path

# ── Таймфреймы и глубина истории ────────────────────────────────────────────
D1_TIMEFRAME = "1d"
H1_TIMEFRAME = "1h"
D1_LIMIT = 30          # свечей D1 для глобального контекста (стратегия №1)
H1_LIMIT = 120         # свечей H1 для локальной картины (~5 дней)

# ── Кеш свечей (в секундах) — чтобы мониторинг не дёргал биржу зря ───────────
CACHE_TTL = {"1h": 300, "1d": 3600}

# ── Аналитика (тренд, уровни, ликвидность) ──────────────────────────────────
EMA_PERIOD = 20         # период EMA для определения глобального тренда
TREND_BAND = 0.001      # мёртвая зона ±0.1% вокруг EMA → 'sideways'
D1_PIVOT_WINDOW = 5     # окно поиска пиков/впадин на D1 (глобальные уровни)
H1_PIVOT_WINDOW = 3     # окно поиска пиков/впадин на H1 (локальные уровни)
LEVEL_MATCH_TOL = 0.001  # совпадение уровня H1 с D1 в пределах 0.1% → 'strong'

# ── Паттерны (Spring / Upthrust) ────────────────────────────────────────────
VOL_LOOKBACK = 20       # сколько свечей берём для среднего объёма
STOP_SPREAD = 0.001     # запас стопа за экстремум свечи пробоя (0.1%)
SIGNAL_DEDUP_MIN = 90   # не дублировать одинаковый сигнал чаще, чем раз в N минут

# ── Трекинг исхода сигналов (2-я волна) ──────────────────────────────────────
SIGNAL_EXPIRE_HOURS = 48  # если за столько часов сигнал не дошёл ни до цели, ни
                          # до стопа — помечаем 'expired' (исход неизвестен)

# ── Стакан (DOM, только крипта) ──────────────────────────────────────────────
ORDERBOOK_LIMIT = 50    # сколько уровней стакана тянем с каждой стороны
ORDERBOOK_TTL = 30      # кеш стакана (сек) — стакан меняется быстро, держим коротко
ORDERBOOK_BALANCE = 0.1  # |дисбаланс| ≤ этого → 'баланс'; больше → перевес стороны
WALL_MULT = 5           # заявка — «стена», если её объём > среднего по стороне × этого

# ── Планировщик (минуты) ────────────────────────────────────────────────────
ANALYZE_EVERY_MIN = 60  # обновление контекста/уровней
MONITOR_EVERY_MIN = 5   # проверка паттернов

# ── Пороги, настраиваемые через /settings (хранятся в settings.json) ─────────
_DEFAULTS = {
    "VOL_MULT": 1.5,        # во сколько раз объём свечи > среднего → аномальный
    "BREAK_PCT": 0.0005,    # минимальная глубина ложного пробоя (0.05%)
    "LIQUIDITY_MULT": 1.5,  # объём зоны ликвидности > среднего × этого
}
_SETTINGS_PATH = Path(__file__).parent / "settings.json"


def _load() -> dict:
    if _SETTINGS_PATH.exists():
        try:
            saved = json.loads(_SETTINGS_PATH.read_text(encoding="utf-8"))
            return {**_DEFAULTS, **saved}
        except Exception:
            return dict(_DEFAULTS)
    return dict(_DEFAULTS)


_runtime = _load()


def get(key: str):
    """Текущее значение настраиваемого порога (с учётом правок через /settings)."""
    return _runtime.get(key, _DEFAULTS.get(key))


def set_value(key: str, value) -> None:
    """Изменить порог и сохранить в settings.json. Используется командой /settings."""
    if key not in _DEFAULTS:
        raise KeyError(key)
    _runtime[key] = value
    _SETTINGS_PATH.write_text(
        json.dumps(_runtime, ensure_ascii=False, indent=2), encoding="utf-8"
    )


def all_settings() -> dict:
    """Все текущие настраиваемые пороги (для отображения в /settings)."""
    return dict(_runtime)
